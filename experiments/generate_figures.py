def exp1_table1_noise():
    """Table 1: Noise robustness."""
    print("\n" + "=" * 70)
    print("Exp 1 / Table 1: Noise Robustness (d=20, T=500)")
    print("=" * 70)

    df_old = pd.read_csv("0328expres/exp1_noise/all_results.csv")
    df_new = pd.read_csv("0328expres/0406/exp1_noise/all_results.csv")
    baselines = df_old[~df_old["method"].str.contains("PRCD")]
    df = pd.concat([df_new, baselines], ignore_index=True)

    paper_methods = ["PRCD-MAP(learn_tau)", "PRCD-MAP(uniform)",
                     "PCMCI+", "DYNOTEARS", "VARLiNGAM"]
    df = df[df["method"].isin(paper_methods)]
    metric = "comb_auroc"
    noise_types = ["gaussian", "laplace", "student_t", "heteroscedastic"]

    rows = []
    for m in paper_methods:
        row = {"Method": DISPLAY.get(m, m)}
        for nt in noise_types:
            sub = df[(df["method"] == m) & (df["noise"] == nt)]
            row[nt.capitalize()] = fmt(sub[metric].mean(), sub[metric].std()) if len(sub) else "---"
        sub_all = df[df["method"] == m]
        row["Avg."] = fmt(sub_all[metric].mean(), sub_all[metric].std())
        rows.append(row)
    tbl = pd.DataFrame(rows)
    tbl.to_csv(os.path.join(OUT_DIR, "exp1_table1_noise.csv"), index=False)
    print(tbl.to_string(index=False))

    # Bar chart
    fig, ax = plt.subplots(figsize=(11, 5))
    x = np.arange(len(noise_types))
    n_m = len(paper_methods)
    w = 0.8 / n_m
    for i, m in enumerate(paper_methods):
        vals = [df[(df["method"]==m) & (df["noise"]==nt)][metric].mean()
                if len(df[(df["method"]==m) & (df["noise"]==nt)]) else 0 for nt in noise_types]
        errs = [df[(df["method"]==m) & (df["noise"]==nt)][metric].std()
                if len(df[(df["method"]==m) & (df["noise"]==nt)]) else 0 for nt in noise_types]
        ax.bar(x + i*w, vals, w, yerr=errs, label=DISPLAY.get(m, m),
               color=COLORS.get(m, "grey"), alpha=0.85, capsize=2)
    ax.set_xticks(x + w*(n_m-1)/2)
    ax.set_xticklabels(["Gaussian", "Laplace", "Student-t", "Heterosced."], fontsize=11)
    ax.set_ylabel("AUROC", fontsize=12)
    ax.set_title("(a) Noise Robustness", fontsize=13, fontweight="bold")
    ax.legend(fontsize=8, loc="lower left", ncol=2)
    ax.grid(True, axis="y", ls=":", alpha=0.4)
    ax.set_ylim(0.55, 0.95)
    plt.tight_layout()
    save_fig("exp1_table1_noise_bar")


def exp1_table2_sample_size():
    """Table 2: Sample size sensitivity — core selling point."""
    print("\n" + "=" * 70)
    print("Exp 1 / Table 2: Sample Size Sensitivity (d=20)")
    print("=" * 70)

    df = pd.read_csv("0328expres/0406/exp1_sample_size/all_results.csv")
    paper_methods = ["PRCD-MAP(learn_tau)", "PRCD-MAP(fixed_tau)",
                     "PCMCI+", "DYNOTEARS", "VARLiNGAM"]
    df = df[df["method"].isin(paper_methods)]
    metric = "comb_auroc"

    for acc in [0.4, 0.6, 0.9]:
        print(f"\n--- Prior Accuracy = {acc} ---")
        rows = []
        for m in paper_methods:
            row = {"Method": DISPLAY.get(m, m)}
            for T in [50, 100, 200, 500]:
                sub = df[(df["method"]==m) & (df["T"]==T) & (df["prior_acc"]==acc)]
                row[f"T={T}"] = fmt(sub[metric].mean(), sub[metric].std()) if len(sub) else "---"
            rows.append(row)
        tbl = pd.DataFrame(rows)
        print(tbl.to_string(index=False))
        tbl.to_csv(os.path.join(OUT_DIR, f"exp1_table2_samplesize_acc{acc}.csv"), index=False)


def exp1_figure2_prior_sweep():
    """Figure 2: Prior accuracy sweep — THE core figure."""
    print("\n" + "=" * 70)
    print("Exp 1 / Figure 2: Prior Quality x Sample Size")
    print("=" * 70)

    df = pd.read_csv("0328expres/0406/exp1_sample_size/all_results.csv")
    metric = "comb_auroc"

    # Combined single figure
    fig, ax = plt.subplots(figsize=(8, 5.5))
    for acc, ls, alpha in [(0.4, "--", 0.6), (0.6, "-.", 0.8), (0.9, "-", 1.0)]:
        sub = df[(df["method"]=="PRCD-MAP(learn_tau)") & (df["prior_acc"]==acc)]
        agg = sub.groupby("T")[metric].agg(["mean","std"]).reset_index()
        ax.errorbar(agg["T"], agg["mean"], yerr=agg["std"],
                    label=f"PRCD-MAP (acc={acc})", marker="o",
                    color=COLORS["PRCD-MAP(learn_tau)"],
                    lw=2.5, capsize=4, markersize=8, linestyle=ls, alpha=alpha)

    for m in ["PCMCI+", "DYNOTEARS"]:
        sub = df[df["method"]==m]
        if sub.empty: continue
        agg = sub.groupby("T")[metric].agg(["mean","std"]).reset_index()
        ax.errorbar(agg["T"], agg["mean"], yerr=agg["std"],
                    label=DISPLAY[m], marker=MARKERS[m], color=COLORS[m],
                    lw=2.5, capsize=4, markersize=8)

    ax.set_xlabel("Sample Size (T)", fontsize=13)
    ax.set_ylabel("AUROC", fontsize=13)
    ax.set_title("Prior Knowledge Compensates for Limited Data",
                 fontsize=14, fontweight="bold")
    ax.legend(fontsize=9, loc="lower right")
    ax.grid(True, ls=":", alpha=0.5)
    ax.set_ylim(0.45, 0.95)
    ax.set_xticks([50, 100, 200, 500])

    # Annotate gap at T=50
    prcd_50 = df[(df["method"]=="PRCD-MAP(learn_tau)") &
                 (df["prior_acc"]==0.9) & (df["T"]==50)][metric].mean()
    pcmci_50 = df[(df["method"]=="PCMCI+") & (df["T"]==50)][metric].mean()
    if not np.isnan(prcd_50) and not np.isnan(pcmci_50):
        ax.annotate(f"+{prcd_50-pcmci_50:.2f}",
                    xy=(50, prcd_50), xytext=(80, prcd_50+0.04),
                    fontsize=11, fontweight="bold", color=COLORS["PRCD-MAP(learn_tau)"],
                    arrowprops=dict(arrowstyle="->", color="#E74C3C"))
    plt.tight_layout()
    save_fig("exp1_figure2_prior_sweep")

    # Also 3-panel version
    fig, axes = plt.subplots(1, 3, figsize=(16, 5), sharey=True)
    for idx, acc in enumerate([0.4, 0.6, 0.9]):
        ax = axes[idx]
        sub_prcd = df[(df["method"]=="PRCD-MAP(learn_tau)") & (df["prior_acc"]==acc)]
        agg = sub_prcd.groupby("T")[metric].agg(["mean","std"]).reset_index()
        ax.errorbar(agg["T"], agg["mean"], yerr=agg["std"],
                    label=f"PRCD-MAP (acc={acc})", marker="o",
                    color=COLORS["PRCD-MAP(learn_tau)"], lw=2.5, capsize=4, markersize=8)
        for m in ["PCMCI+", "DYNOTEARS", "VARLiNGAM"]:
            sub = df[df["method"]==m]
            if sub.empty: continue
            agg_b = sub.groupby("T")[metric].agg(["mean","std"]).reset_index()
            ax.errorbar(agg_b["T"], agg_b["mean"], yerr=agg_b["std"],
                        label=DISPLAY[m], marker=MARKERS[m], color=COLORS[m],
                        lw=2, capsize=3, markersize=7, alpha=0.8)
        ax.set_xlabel("Sample Size (T)", fontsize=12)
        if idx == 0: ax.set_ylabel("AUROC", fontsize=12)
        ax.set_title(f"Prior Accuracy = {acc}", fontsize=13, fontweight="bold")
        ax.legend(fontsize=8, loc="lower right")
        ax.grid(True, ls=":", alpha=0.5)
        ax.set_ylim(0.45, 0.95)
        ax.set_xticks([50, 100, 200, 500])
    fig.suptitle("Effect of Sample Size and Prior Quality on Causal Discovery",
                 fontsize=15, fontweight="bold", y=1.02)
    plt.tight_layout()
    save_fig("exp1_figure2_prior_sweep_3panel")


