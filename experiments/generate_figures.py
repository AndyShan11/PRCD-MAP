def exp3_table4_ablation():
    """Table 4: Ablation study."""
    print("\n" + "=" * 70)
    print("Exp 3 / Table 4: Ablation Study")
    print("=" * 70)

    synth_path = "0328expres/0406/exp4_results/summary_synthetic_overall.csv"
    lorenz_path = "0328expres/0406/exp4_results/summary_lorenz96.csv"

    if not os.path.exists(synth_path):
        print("  SKIP — ablation results not found")
        return

    df_s = pd.read_csv(synth_path)
    df_l = pd.read_csv(lorenz_path) if os.path.exists(lorenz_path) else pd.DataFrame()

    # Combined table
    rows = []
    variant_order = ["NoPrior", "FixedTau", "FullModel", "NoL1", "LagsOnly",
                     "HardMask", "NoWarmStart", "NoLamSched"]
    for v in variant_order:
        row = {"Variant": v}
        s = df_s[df_s["variant_name"] == v]
        if not s.empty:
            row["Synth AUROC"] = f"{s['comb_auroc_mean'].values[0]:.3f}"
            row["Synth F1"] = f"{s['comb_f1_mean'].values[0]:.3f}"
        else:
            row["Synth AUROC"] = "---"
            row["Synth F1"] = "---"

        if not df_l.empty:
            l = df_l[df_l["variant_name"] == v]
            if not l.empty:
                row["Lorenz AUROC"] = f"{l['auroc_mean'].values[0]:.3f}"
                row["Lorenz F1"] = f"{l['f1_mean'].values[0]:.3f}"
            else:
                row["Lorenz AUROC"] = "---"
                row["Lorenz F1"] = "---"
        rows.append(row)

    tbl = pd.DataFrame(rows)
    tbl.to_csv(os.path.join(OUT_DIR, "exp3_table4_ablation.csv"), index=False)
    print(tbl.to_string(index=False))

    # Bar chart — synthetic
    fig, axes = plt.subplots(1, 2, figsize=(14, 5.5))
    present = [v for v in variant_order if v in df_s["variant_name"].values]
    n_v = len(present)
    for metric, ax, yl in [("comb_auroc_mean", axes[0], "AUROC"),
                            ("comb_f1_mean", axes[1], "F1")]:
        vals = [df_s[df_s["variant_name"]==v][metric].values[0] for v in present]
        colors_list = [VARIANT_COLORS.get(v, "grey") for v in present]
        bars = ax.bar(range(n_v), vals, color=colors_list, alpha=0.85,
                      edgecolor="white", linewidth=0.8)
        ax.set_xticks(range(n_v))
        ax.set_xticklabels(present, rotation=35, ha="right", fontsize=9)
        ax.set_ylabel(yl, fontsize=12)
        ax.grid(True, axis="y", ls=":", alpha=0.4)
        for bar, val in zip(bars, vals):
            ax.text(bar.get_x()+bar.get_width()/2, bar.get_height(),
                    f"{val:.3f}", ha="center", va="bottom", fontsize=8)
    fig.suptitle("Ablation Study: Synthetic Data (d=10,20)",
                 fontsize=14, fontweight="bold")
    plt.tight_layout()
    save_fig("exp3_table4_ablation_synth_bar")

    # Bar chart — Lorenz
    if not df_l.empty:
        present_l = [v for v in variant_order if v in df_l["variant_name"].values]
        n_vl = len(present_l)
        fig, axes = plt.subplots(1, 2, figsize=(14, 5.5))
        for metric, ax, yl in [("auroc_mean", axes[0], "AUROC"),
                                ("f1_mean", axes[1], "F1")]:
            vals = [df_l[df_l["variant_name"]==v][metric].values[0] for v in present_l]
            colors_list = [VARIANT_COLORS.get(v, "grey") for v in present_l]
            bars = ax.bar(range(n_vl), vals, color=colors_list, alpha=0.85,
                          edgecolor="white", linewidth=0.8)
            ax.set_xticks(range(n_vl))
            ax.set_xticklabels(present_l, rotation=35, ha="right", fontsize=9)
            ax.set_ylabel(yl, fontsize=12)
            ax.grid(True, axis="y", ls=":", alpha=0.4)
            for bar, val in zip(bars, vals):
                ax.text(bar.get_x()+bar.get_width()/2, bar.get_height(),
                        f"{val:.3f}", ha="center", va="bottom", fontsize=8)
        fig.suptitle("Ablation Study: Lorenz-96",
                     fontsize=14, fontweight="bold")
        plt.tight_layout()
        save_fig("exp3_table4_ablation_lorenz_bar")


