def app_B_scale():
    """App B: Scale AUROC."""
    print("\n[App B] Scale: d vs AUROC")
    df_old = pd.read_csv("0328expres/exp1_scale/all_results.csv")
    df_new = pd.read_csv("0328expres/0405/exp1_scale/all_results.csv")
    baselines = df_old[~df_old["method"].str.contains("PRCD")]
    df = pd.concat([df_new, baselines], ignore_index=True)
    metric = "comb_auroc"
    methods = ["PRCD-MAP(learn_tau)", "PRCD-MAP(uniform)", "DYNOTEARS", "VARLiNGAM"]
    df = df[df["method"].isin(methods)]

    rows = []
    for m in methods:
        row = {"Method": DISPLAY.get(m, m)}
        for d_val in sorted(df["d"].unique()):
            sub = df[(df["method"]==m) & (df["d"]==d_val)]
            row[f"d={d_val}"] = fmt(sub[metric].mean(), sub[metric].std()) if len(sub) else "---"
        rows.append(row)
    tbl = pd.DataFrame(rows)
    tbl.to_csv(os.path.join(OUT_DIR, "appB_scale.csv"), index=False)
    print(tbl.to_string(index=False))

    fig, ax = plt.subplots(figsize=(8, 5))
    for m in methods:
        sub = df[df["method"]==m]
        agg = sub.groupby("d")[metric].agg(["mean","std"]).reset_index()
        ax.errorbar(agg["d"], agg["mean"], yerr=agg["std"],
                    label=DISPLAY.get(m,m), marker=MARKERS.get(m,"x"),
                    color=COLORS.get(m,"grey"), lw=2.5, capsize=4, markersize=8)
    ax.set_xlabel("Number of Variables (d)", fontsize=13)
    ax.set_ylabel("AUROC", fontsize=13)
    ax.set_title("AUROC vs Dimension (PCMCI+ cannot run at d>=50)",
                 fontsize=13, fontweight="bold")
    ax.legend(fontsize=10)
    ax.grid(True, ls=":", alpha=0.5)
    ax.set_xticks(sorted(df["d"].unique()))
    plt.tight_layout()
    save_fig("appB_scale_auroc")


