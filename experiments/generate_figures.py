def exp4_figure1_scalability():
    """Figure 1: Runtime vs dimension."""
    print("\n" + "=" * 70)
    print("Exp 4 / Figure 1: Scalability (d vs time)")
    print("=" * 70)

    # Timing not affected by score-cal, use 0405 data
    df_new = pd.read_csv("0328expres/0405/exp5_results/partA_scalability.csv")
    df_old = pd.read_csv("0328expres/exp5_results/partA_scalability.csv")

    baselines = df_old[~df_old["method"].str.contains("PRCD|NAM|NGC")]
    prcd_new = df_new[df_new["method"] == "PRCD-MAP(learn_tau)"]
    df = pd.concat([prcd_new, baselines], ignore_index=True)

    paper_methods = ["PRCD-MAP(learn_tau)", "PCMCI+", "DYNOTEARS", "VARLiNGAM"]
    df = df[df["method"].isin(paper_methods)]

    # Print
    for m in paper_methods:
        sub = df[df["method"]==m]
        times = sub.groupby("d")["time"].mean()
        time_str = "  ".join([f"d={d}: {t:.1f}s" for d, t in times.items()])
        print(f"  {DISPLAY.get(m,m):<20} {time_str}")

    # Figure
    fig, ax = plt.subplots(figsize=(8, 5.5))
    for m in paper_methods:
        sub = df[df["method"]==m]
        agg = sub.groupby("d")["time"].agg(["mean","std"]).reset_index()
        ax.errorbar(agg["d"], agg["mean"], yerr=agg["std"],
                    label=DISPLAY.get(m,m), marker=MARKERS.get(m,"x"),
                    color=COLORS.get(m,"grey"), lw=2.5, capsize=4, markersize=8)

    ax.set_xlabel("Number of Variables (d)", fontsize=13)
    ax.set_ylabel("Runtime (seconds)", fontsize=13)
    ax.set_title("Scalability: Runtime vs Dimension", fontsize=14, fontweight="bold")
    ax.set_yscale("log")
    ax.legend(fontsize=10, loc="upper left")
    ax.grid(True, ls=":", alpha=0.5)
    ax.set_xticks([10, 20, 50])
    ax.set_xticklabels(["10", "20", "50"], fontsize=11)

    # Annotate ratio at d=50
    pcmci_50 = df[(df["method"]=="PCMCI+") & (df["d"]==50)]["time"].mean()
    prcd_50 = df[(df["method"]=="PRCD-MAP(learn_tau)") & (df["d"]==50)]["time"].mean()
    if not np.isnan(pcmci_50) and not np.isnan(prcd_50):
        ratio = pcmci_50 / prcd_50
        ax.annotate(f"{ratio:.0f}x faster", xy=(50, prcd_50),
                    xytext=(42, prcd_50*3), fontsize=10, fontweight="bold",
                    color=COLORS["PRCD-MAP(learn_tau)"],
                    arrowprops=dict(arrowstyle="->", color="#E74C3C"))
    plt.tight_layout()
    save_fig("exp4_figure1_scalability")


