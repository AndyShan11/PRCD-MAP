def exp2_table3_causaltime():
    """Table 3: CausalTime real-world benchmarks."""
    print("\n" + "=" * 70)
    print("Exp 2 / Table 3: CausalTime Real-World Benchmarks")
    print("=" * 70)

    # New PRCD-MAP results (0406, no score-cal)
    df = pd.read_csv("0328expres/0406/exp2_results/exp2_causaltime_results.csv")

    # Old baselines from 0405 (PCMCI+, DYNOTEARS, VARLiNGAM, NGC)
    df_old = pd.read_csv("0328expres/0405/exp2_results/exp2_causaltime_results.csv")
    baselines = df_old[~df_old["method"].str.contains("PRCD")]
    df = pd.concat([df, baselines], ignore_index=True)

    # Merge RHINO
    rhino_path = "0328expres/rhino/rhino_exp2_causaltime.csv"
    if os.path.exists(rhino_path):
        df = pd.concat([df, pd.read_csv(rhino_path)], ignore_index=True)

    # For PRCD variants with prior_acc, take best acc per dataset
    for m in df[df["method"].str.contains("PRCD")]["method"].unique():
        sub = df[df["method"]==m]
        if "prior_acc" in sub.columns and sub["prior_acc"].nunique() > 1:
            best_acc = sub.groupby("prior_acc")["auroc"].mean().idxmax()
            df = df.drop(sub[sub["prior_acc"] != best_acc].index)

    paper_methods = ["PRCD-MAP(learn_tau)", "PRCD-MAP(uniform)",
                     "PCMCI+", "DYNOTEARS", "VARLiNGAM", "RHINO", "NGC"]
    df_show = df[df["method"].isin(paper_methods)]
    datasets = sorted(df_show["bench"].unique())

    rows = []
    for m in paper_methods:
        row = {"Method": DISPLAY.get(m, m)}
        for ds in datasets:
            sub = df_show[(df_show["method"]==m) & (df_show["bench"]==ds)]
            row[ds.replace("CausalTime_","")] = fmt(sub["auroc"].mean(), sub["auroc"].std()) if len(sub) else "---"
        sub_all = df_show[df_show["method"]==m]
        row["Avg."] = fmt(sub_all["auroc"].mean(), sub_all["auroc"].std())
        rows.append(row)

    tbl = pd.DataFrame(rows)
    tbl.to_csv(os.path.join(OUT_DIR, "exp2_table3_causaltime.csv"), index=False)
    print(tbl.to_string(index=False))

    # Bar chart
    ds_labels = [d.replace("CausalTime_","") for d in datasets]
    fig, ax = plt.subplots(figsize=(10, 5.5))
    x = np.arange(len(datasets))
    n_m = len(paper_methods)
    w = 0.8 / n_m
    for i, m in enumerate(paper_methods):
        vals = [df_show[(df_show["method"]==m)&(df_show["bench"]==ds)]["auroc"].mean()
                if len(df_show[(df_show["method"]==m)&(df_show["bench"]==ds)]) else 0 for ds in datasets]
        errs = [df_show[(df_show["method"]==m)&(df_show["bench"]==ds)]["auroc"].std()
                if len(df_show[(df_show["method"]==m)&(df_show["bench"]==ds)]) else 0 for ds in datasets]
        ax.bar(x+i*w, vals, w, yerr=errs, label=DISPLAY.get(m,m),
               color=COLORS.get(m,"grey"), alpha=0.85, capsize=2)
    ax.set_xticks(x + w*(n_m-1)/2)
    ax.set_xticklabels(ds_labels, fontsize=11)
    ax.set_ylabel("AUROC", fontsize=12)
    ax.set_title("CausalTime Benchmarks (T=400)", fontsize=14, fontweight="bold")
    ax.legend(fontsize=7, loc="upper right", ncol=2)
    ax.grid(True, axis="y", ls=":", alpha=0.4)
    ax.set_ylim(0.35, 0.85)
    plt.tight_layout()
    save_fig("exp2_table3_causaltime_bar")


