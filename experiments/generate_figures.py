DISPLAY = {
    "PRCD-MAP(learn_tau)": "PRCD-MAP",
    "PRCD-MAP(fixed_tau)": r"PRCD-MAP ($\tau$=1)",
    "PRCD-MAP(uniform)":   "PRCD-MAP (no prior)",
    "PCMCI+": "PCMCI+", "DYNOTEARS": "DYNOTEARS",
    "VARLiNGAM": "VARLiNGAM", "RHINO": "RHINO", "NGC": "NGC",
}


def fmt(val, std=None):
    if pd.isna(val): return "---"
    if std is not None and not pd.isna(std):
        return f"{val:.3f}$\\pm${std:.3f}"
    return f"{val:.3f}"


def save_fig(name):
    for ext in ["pdf", "png"]:
        plt.savefig(os.path.join(OUT_DIR, f"{name}.{ext}"),
                    bbox_inches="tight", dpi=300 if ext == "png" else 600)
    plt.close()
    print(f"  >>> {name}.pdf/.png")


