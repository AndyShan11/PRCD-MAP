def app_A_nonlinear():
    """App A: Nonlinear comparison."""
    print("\n[App A] Nonlinear vs Linear")
    df_old = pd.read_csv("0328expres/exp1_nonlinear/all_results.csv")
    df_new = pd.read_csv("0328expres/0405/exp1_nonlinear/all_results.csv")
    baselines = df_old[~df_old["method"].str.contains("PRCD")]
    df = pd.concat([df_new, baselines], ignore_index=True)
    metric = "comb_auroc"
    methods = ["PRCD-MAP(learn_tau)", "PCMCI+", "DYNOTEARS", "VARLiNGAM"]
    df = df[df["method"].isin(methods)]

