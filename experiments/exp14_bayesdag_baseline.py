"""
exp14_bayesdag_baseline.py — run BayesDAG (Annadani et al., NeurIPS 2023) on the
same benchmarks PRCD-MAP-release uses, then save AUROC/F1/SHD JSON for direct
comparison in Table-1 / Table-2.

Strategy: call BayesDAG's Python API directly (BayesDAGLinear.run_train +
get_adj_matrix). No CLI, no config JSONs, no mlflow tracker spinup.

Usage (must be run with the bayesdag conda env's python):
  python exp14_bayesdag_baseline.py \
      --bench medical --seeds 10 --max-epochs 100 --num-chains 4

Benches (data layouts):
  aqi traffic medical                   -> CausalTime (loaded via PRCD utils)
  lorenz nonlinear                      -> generated by PRCD's helpers
  cross_d20                             -> NOTEARS-style, n=500, edge_prob=0.15

Output: results/exp14_bayesdag/{bench}_{tag}_seed{seed}.json
"""
import os, sys, json, time, argparse, traceback, tempfile
from pathlib import Path
from typing import Tuple, Optional

import numpy as np

# ----------------------------------------------------------------------------
# Paths — adjust here once if your layout differs.
# ----------------------------------------------------------------------------
BAYESDAG_SRC  = "/path/to/bayesdag/src"
PRCD_RELEASE  = "."
CAUSALTIME    = "./data/causaltime"
RESULTS_DIR   = Path(PRCD_RELEASE) / "experiments" / "results" / "exp14_bayesdag"

# Make PRCD utils + BayesDAG src importable
sys.path.insert(0, BAYESDAG_SRC)
sys.path.insert(0, os.path.join(PRCD_RELEASE, "src"))
sys.path.insert(0, os.path.join(PRCD_RELEASE, "experiments"))


# ----------------------------------------------------------------------------
# Data loaders — produce (X, B_true) where X: (T, d) numpy, B_true: (d, d) {0,1}
# ----------------------------------------------------------------------------
def load_bench(bench: str, seed: int, n_cross: int = 500
               ) -> Tuple[np.ndarray, np.ndarray]:
    if bench in ("aqi", "traffic", "medical"):
        from utils_trust import load_causaltime
        ds_name = {"aqi": "AQI", "traffic": "Traffic", "medical": "Medical"}[bench]
        X, B = load_causaltime(CAUSALTIME, ds_name, n_samples=10)
        return X.astype(np.float32), (np.abs(B) > 1e-10).astype(int)

    if bench == "lorenz":
        from utils_trust import generate_lorenz96
        X, B = generate_lorenz96(d=20, T=1000, seed=seed)
        return X.astype(np.float32), (np.abs(B) > 1e-10).astype(int)

    if bench == "nonlinear":
        from utils_trust import simulate_svar_nonlinear, make_er_dag
        W0 = make_er_dag(20, edge_prob=0.15, seed=seed)
        Wk = [np.zeros_like(W0)]
        X = simulate_svar_nonlinear(T=1000, W0=W0, Wk=Wk, seed=seed)
        return X.astype(np.float32), (np.abs(W0) > 1e-10).astype(int)

    if bench == "cross_d20":
        from exp5_cross_sectional import simulate_cross_sectional
        from utils import make_er_dag
        W0 = make_er_dag(20, edge_prob=0.15, seed=seed)
        X = simulate_cross_sectional(n=n_cross, W0=W0, noise_type="gaussian", seed=seed)
        return X.astype(np.float32), (np.abs(W0) > 1e-10).astype(int)

    raise ValueError(f"unknown bench: {bench}")


# ----------------------------------------------------------------------------
# Build BayesDAG's Variables / CausalDataset directly from numpy
# ----------------------------------------------------------------------------
def make_variables(X: np.ndarray):
    from causica.datasets.variables import Variables, Variable
    d = X.shape[1]
    lo = X.min(axis=0)
    hi = X.max(axis=0)
    var_list = [
        Variable(
            name=f"x{i}",
            query=True,
            type="continuous",
            lower=float(lo[i]),
            upper=float(hi[i]),
            always_observed=True,
        )
        for i in range(d)
    ]
    return Variables(var_list)


def make_dataset(X: np.ndarray, B_true: np.ndarray, val_frac: float = 0.1):
    from causica.datasets.dataset import CausalDataset
    variables = make_variables(X)
    T = X.shape[0]
    n_val = max(1, int(val_frac * T))
    train, test = X[:-n_val], X[-n_val:]
    train_mask = np.ones_like(train)
    test_mask  = np.ones_like(test)
    subgraph_mask = np.ones_like(B_true, dtype=int)

    ds = CausalDataset(
        train_data=train, train_mask=train_mask,
        test_data=test,   test_mask=test_mask,
        adjacency_data=B_true.astype(int),
        subgraph_data=subgraph_mask,
        intervention_data=None,
        counterfactual_data=None,
        variables=variables,
    )
    return ds


# ----------------------------------------------------------------------------
# Train BayesDAG and extract posterior-mean adjacency
# ----------------------------------------------------------------------------
def run_bayesdag(X, B_true, model_type, max_epochs, num_chains,
                 lr, batch_size, save_dir, device):
    import torch
    from causica.models.bayesdag.bayesdag_linear    import BayesDAGLinear
    from causica.models.bayesdag.bayesdag_nonlinear import BayesDAGNonLinear

    save_dir.mkdir(parents=True, exist_ok=True)
    dataset = make_dataset(X, B_true)
    variables = dataset.variables

    Cls = BayesDAGLinear if model_type == "linear" else BayesDAGNonLinear
    model_kwargs = dict(
        model_id="exp14",
        variables=variables,
        save_dir=str(save_dir),
        device=device,
        lambda_sparse=1.0,
        num_chains=num_chains,
        scale_noise=0.1,
        scale_noise_p=1.0,
    )
    if model_type == "linear":
        model_kwargs.update(dict(sinkhorn_n_iter=300, VI_norm=False,
                                 input_perm=False, sparse_init=False))

    model = Cls(**model_kwargs).to(device)

    train_config = {
        "max_epochs":            max_epochs,
        "batch_size":            batch_size,
        "learning_rate":         lr,
        "stardardize_data_mean": False,
        "stardardize_data_std":  False,
    }
    model.run_train(dataset, train_config_dict=train_config)

    # Posterior samples → mean edge probability
    adj_samples, is_dag = model.get_adj_matrix(samples=100)
    # adj_samples: (100, d, d) bool
    P = adj_samples.astype(np.float64).mean(axis=0)
    return P, int(is_dag.sum())


# ----------------------------------------------------------------------------
# Main
# ----------------------------------------------------------------------------
def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--bench", required=True,
                    choices=["aqi", "traffic", "medical", "lorenz", "nonlinear", "cross_d20"])
    ap.add_argument("--seeds", type=int, nargs="+", required=True)
    ap.add_argument("--n", type=int, default=500, help="cross_d20: sample size")
    ap.add_argument("--variant", choices=["linear", "nonlinear"], default="linear")
    ap.add_argument("--max-epochs", type=int, default=100)
    ap.add_argument("--num-chains", type=int, default=4,
                    help="SG-MCMC chains. 4 fits comfortably in 11GB; 8 for d<=20.")
    ap.add_argument("--lr",         type=float, default=1e-3)
    ap.add_argument("--batch-size", type=int,   default=128)
    args = ap.parse_args()

    import torch
    gpu_id = int(os.environ.get("CUDA_VISIBLE_DEVICES", "0").split(",")[0])
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

    RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    tag = f"n{args.n}" if args.bench == "cross_d20" else args.variant
    print(f"=== bench={args.bench} variant={args.variant} tag={tag} "
          f"seeds={args.seeds} cuda_visible={gpu_id} device={device} ===",
          flush=True)

    for seed in args.seeds:
        out_json = RESULTS_DIR / f"{args.bench}_{tag}_seed{seed}.json"
        if out_json.exists():
            print(f"[SKIP] {out_json.name} exists", flush=True)
            continue

        t0 = time.time()
        try:
            X, B = load_bench(args.bench, seed, n_cross=args.n)
            print(f"[seed {seed}] X={X.shape} B={B.shape} edges={int(B.sum())}",
                  flush=True)

            with tempfile.TemporaryDirectory(prefix="bayesdag_") as tmp:
                P, n_dags = run_bayesdag(
                    X, B,
                    model_type=args.variant,
                    max_epochs=args.max_epochs,
                    num_chains=args.num_chains,
                    lr=args.lr, batch_size=args.batch_size,
                    save_dir=Path(tmp), device=device,
                )

            # Score = mean posterior edge prob; metrics from PRCD utils
            from utils import compute_all_metrics
            m = compute_all_metrics(B, P)
            m = {k: float(v) if hasattr(v, "item") else v for k, v in m.items()}
            m.update({
                "bench": args.bench, "variant": args.variant, "tag": tag,
                "seed": seed,
                "n_dags": int(n_dags),
                "runtime_sec": round(time.time() - t0, 2),
                "d": int(B.shape[0]), "T": int(X.shape[0]),
            })
            out_json.write_text(json.dumps(m, indent=2))
            print(f"[seed {seed}] OK  AUROC={m.get('auroc'):.3f}  "
                  f"F1={m.get('f1_opt'):.3f}  SHD={m.get('shd')}  "
                  f"n_dags={n_dags}  ({m['runtime_sec']:.0f}s) -> {out_json.name}",
                  flush=True)

        except Exception as e:
            print(f"[seed {seed}] FAIL: {e}", flush=True)
            traceback.print_exc()
            out_json.write_text(json.dumps({
                "bench": args.bench, "variant": args.variant, "tag": tag,
                "seed": seed,
                "error": str(e),
                "trace": traceback.format_exc().splitlines()[-8:],
                "runtime_sec": round(time.time() - t0, 2),
            }, indent=2))


if __name__ == "__main__":
    main()
